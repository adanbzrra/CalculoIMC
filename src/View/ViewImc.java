    private void jbCalcularActionPerformedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCalcularActionPerformedActionPerformed
        // TODO add your handling code here:
       
        modelImc.setAltura(Double.parseDouble(jtfAltura.getText()));
        modelImc.setPeso(Double.parseDouble(jtfPeso.getText()));
        modelImc.calcular();
        
        if (modelImc.resultado <= 18.5) {
            
            jTextArea1.setText("SEU IMC √â " + new DecimalFormat(",##").format(modelImc.resultado) + "\n" + "MENOR QUE 18,5" + "\n" + "SUA CLASSIFICA√á√ÉO √â DE MAGREZA" );
            
        } else if (modelImc.resultado >= 18.5 && modelImc.resultado <= 24.9) {
            jTextArea1.setText("SEU IMC √â " + new DecimalFormat(",##").format(modelImc.resultado) + "\n" + "ENTRE 18,5 E 24,9" + "\n" + "SUA CLASSIFICA√á√ÉO EST√Å DENTRO DO NORMAL" );

        } else if (modelImc.resultado >= 25.0 && modelImc.resultado<= 29.9) {
            
            jTextArea1.setText("SEU IMC √â " + new DecimalFormat(",##").format(modelImc.resultado) + "\n" + "ENTRE 25,0 E 29,9" + "\n" + "SUA CLASSIFICA√á√ÉO √â SOBREPESO I" );
   
        } else if (modelImc.resultado >= 30.0 && modelImc.resultado <= 39.9) {
            
            jTextArea1.setText("SEU IMC √â " + new DecimalFormat(",##").format(modelImc.resultado) + "\n" + "ENTRE 30,0 E 39,9" + "\n" + "SUA CLASSIFICA√á√ÉO √â SOBREPESO II" );

        } else if (modelImc.resultado >= 40.0) {
            
            jTextArea1.setText("SEU IMC √â" + new DecimalFormat(",##").format(modelImc.resultado) + "\n" + "MAIOR QUE 40,0" + "\n" + "SUA CLASSIFICA√á√ÉO √â OBESIDADE GRAVE III" );
        }
    }//GEN-LAST:event_jbCalcularActionPerformedActionPerformed

